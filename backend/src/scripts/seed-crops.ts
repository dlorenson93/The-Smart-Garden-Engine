async function seed() {
  console.log('ðŸŒ± Starting crop seed...');

  for (const crop of crops) {
    const existing = await prisma.crop.findFirst({
      where: { name: crop.name },
    });

    if (existing) {
      console.log(`  âœ“ ${crop.name} already exists, skipping`);
    } else {
      await prisma.crop.create({ data: crop });
      console.log(`  + Created ${crop.name}`);
    }
  }

  const count = await prisma.crop.count();
  console.log(`\nâœ… Seed complete! Total crops in database: ${count}`);
}

seed()
  .catch((error) => {
    console.error('âŒ Seed failed:', error);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
